from __future__ import annotations

import argparse
import sys
from datetime import datetime
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Unified benchmark runner skeleton (QA/TF-IDF/compare).",
    )
    parser.add_argument(
        "--mode",
        choices=["qa", "tfidf", "compare", "full"],
        default="full",
        help="Run only QA, only TF-IDF official, compare pipeline, or all.",
    )
    parser.add_argument(
        "--timestamp",
        default=datetime.now().strftime("%Y%m%d_%H%M%S"),
        help="Timestamp to use for QA/TF-IDF artifact naming.",
    )
    parser.add_argument(
        "--out-dir",
        default=str(ROOT / "notebooks" / "results" / "official" / "validation"),
        help="Output directory for QA/TF-IDF official artifacts.",
    )
    return parser.parse_args()


def run_qa_and_tfidf(mode: str, out_dir: Path, ts: str) -> list[Path]:
    from scripts.generate_official_validation_artifacts import build_qa_artifact, build_tfidf_artifacts
    from scripts.generate_short_error_analysis import generate_tfidf_error_analysis_from_report

    generated_paths: list[Path] = []
    if mode in {"qa", "full"}:
        generated_paths.append(build_qa_artifact(ROOT, out_dir, ts))
    if mode in {"tfidf", "full"}:
        summary_path, detail_path, report_path = build_tfidf_artifacts(ROOT, out_dir, ts)
        generated_paths.extend([summary_path, detail_path, report_path])
        generated_paths.append(generate_tfidf_error_analysis_from_report(report_path))
    return generated_paths


def main() -> None:
    args = parse_args()
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    generated_paths = run_qa_and_tfidf(args.mode, out_dir, args.timestamp)
    if args.mode in {"compare", "full"}:
        from scripts.benchmark_tfidf_vs_textrank import run_compare_pipeline

        generated_paths.extend(run_compare_pipeline(out_dir=out_dir, ts=args.timestamp))

    if generated_paths:
        print("Generated by QA/TF-IDF branch:")
        for path in generated_paths:
            print(f"- {path}")


if __name__ == "__main__":
    main()
